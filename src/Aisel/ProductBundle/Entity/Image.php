    /**
     * Set file
     *
     * @param array $file
     * @return Image
     */
    public function setFile($file)
    {
        $this->file = $file;

        return $this;
    }

    /**
     * Get file
     *
     * @return array 
     */
    public function getFile()
    {
        return $this->file;
    }
}
